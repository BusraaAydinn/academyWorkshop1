public class whileLoop
{
    public static void main(String[] args)
    {
        int counter = 5;

        // While loop
        while (counter < 5)
        {
            System.out.println("While döngüsü: Sayacın değeri: " + counter);
            counter++;
        }
        System.out.println("While döngüsü sona erdi. Sayacın son değeri: " + counter);
    }

}




